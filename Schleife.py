summe = 0
counter = 0
while counter < 100: # so langer der Counter größer 0 ist mach das darunter
   print(counter) # die Reihenfolge bei Schleifen ist Wichtig! Die definition wie viel runter gezählt gehört unter print.
   counter = counter + 1 #alt.: counter - = 1
   summe += counter
print ("Summe:", summe)
   
